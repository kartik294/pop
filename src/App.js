function WatchedMovie({ movie }) {
  if (!movie) {
    return <div>No movie data available</div>;
  }

  const { poster, title, imdbRating, userRating, runtime } = movie;

  if (
    typeof poster === "undefined" ||
    typeof title === "undefined" ||
    typeof imdbRating === "undefined" ||
    typeof userRating === "undefined" ||
    typeof runtime === "undefined"
  ) {
    return <div>Movie data is incomplete</div>;
  }

  return (
    <li>
      <img src={poster} alt={`${title} poster`} />
      <h3>{title}</h3>
      <div>
        <p>
          <span role="img" aria-label="star">
            ‚≠êÔ∏è
          </span>
          <span>{imdbRating.toFixed(2)}</span>
        </p>
        <p>
          <span role="img" aria-label="star">
            üåü
          </span>
          <span>{userRating.toFixed(2)}</span>
        </p>
        <p>
          <span role="img" aria-label="hourglass">
            ‚è≥
          </span>
          <span>{runtime.toFixed(2)} min</span>
        </p>
      </div>
    </li>
  );
}
